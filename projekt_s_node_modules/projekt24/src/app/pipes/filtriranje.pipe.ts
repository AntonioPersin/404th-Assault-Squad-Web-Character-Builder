import { Pipe, PipeTransform } from '@angular/core';

@Pipe({
  name: 'filtriranje'
})
export class FiltriranjePipe implements PipeTransform {

  transform(polje: any[], flag: boolean, userId: string): any[] {
    if(!Array.isArray(polje)){
      return; 
    }
    if(flag){
      polje=polje.filter(elem=>elem.userId==userId);
    }
    return polje;
  }

}
